<template><div><h1 id="_2-8-双端队列" tabindex="-1"><a class="header-anchor" href="#_2-8-双端队列" aria-hidden="true">#</a> 2.8 双端队列</h1>
<h2 id="_2-8-1-双端队列的定义" tabindex="-1"><a class="header-anchor" href="#_2-8-1-双端队列的定义" aria-hidden="true">#</a> 2.8.1 双端队列的定义</h2>
<p><code v-pre>线性表</code>：任何位置插入删除</p>
<p>栈、队列、双端队列都是只能两端插入删除的线性表</p>
<p><code v-pre>栈</code>：一端插入删除</p>
<p><code v-pre>共享栈</code>：栈的变种，两端为栈底，中间为栈顶，只能两端向中间插入删除</p>
<p><code v-pre>队列</code>：一端插入，另一端删除</p>
<p><code v-pre>双端队列</code>：两端都可插入删除</p>
<p><code v-pre>输入受限的双端队列</code>：一端插入，两端删除</p>
<p><code v-pre>输出受限的双端队列</code>：两端插入，一端删除</p>
